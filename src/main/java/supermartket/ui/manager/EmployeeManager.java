    public void filltoTable(List<Employee> list) {
        DefaultTableModel tbl = (DefaultTableModel) tblEmployee.getModel();
        tbl.setRowCount(0);
        for (Employee employee : list) {
            Object[] values = {
                employee.getEmployeeID(),
                employee.getFullName(),
                employee.getRole(),
                employee.getPhone(),
                employee.getGender() ? "Nam" : "Nữ",
                employee.getEmail(),
                employee.getStartDate(),
                employee.getStatus()
            };
            tbl.addRow(values);
        }
    }

    @Override
    public void delete(String id) {
        dao.deleteById(id);
        fillTable(currentPage);
    }

    @Override
    public void update(String id) {
        Employee emp = dao.findById(id);
        UpdateEmployeeJDialog dialog = new UpdateEmployeeJDialog(null, true, emp);
        dialog.setLocationRelativeTo(null);
        dialog.setVisible(true);
        fillTable(currentPage);
    }
