    public void fillTable(int page) {
        String search = txtSearch.getText().trim();
        String status = cboStatus.getSelectedItem().equals(cboStatus.getItemAt(0).toString()) ? null : cboStatus.getSelectedItem().toString();
        
        SearchEmployeeDTO dto = new SearchEmployeeDTO(search, status , page);
        List<Employee> list = dao.findAll(dto);
        int count = dao.getTotalItem(dto).getCount();
        int limit = 10;
        int totalPage = (int) Math.ceil(count / limit);
        pagination1.setPagegination(page, totalPage);
        filltoTable(list);
    }

