    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String oldPass = new String(txtOldPassword.getPassword()).trim();
        String newPass = new String(txtNewPassword.getPassword()).trim();
        String confirmPass = new String(txtConfirmPassword.getPassword()).trim();

        if (oldPass.isEmpty() || newPass.isEmpty() || confirmPass.isEmpty()) {
            XDialog.alert("Không được để trống các trường mật khẩu");
            return;
        }

        if (!newPass.equals(confirmPass)) {
            XDialog.alert("Mật khẩu mới và xác nhận mật khẩu không khớp");
            return;
        }

        if (newPass.length() < 6) {
            XDialog.alert("Mật khẩu mới phải có ít nhất 6 ký tự");
            return;
        }

        // Giả sử bạn có biến currentUser lưu thông tin user đang đăng nhập
        if (!XAuth.user.getPassword().equals(oldPass)) {
            XDialog.alert("Mật khẩu cũ không đúng");
            return;
        }

        try {
            dao.changePassword(new ChangePassworDTO(XAuth.user.getEmployeeID(), newPass));
            XAuth.user.setPassword(newPass);
            XDialog.alert("Đổi mật khẩu thành công");
            resetForm();
        } catch (Exception e) {
            XDialog.alert("Đổi mật khẩu thất bại");
        }

    }//GEN-LAST:event_jButton2ActionPerformed


