    public void fillToTable(Invoice invoice) {
        DefaultTableModel tbl = (DefaultTableModel) tblProduct.getModel();
        tbl.setRowCount(0);

        txtInvoiceId.setText(invoice.getInvoiceID());
        txtCustomer.setText(invoice.getCustomerID());
        txtEmployee.setText(invoice.getEmployeeID());
        txtPaymentMethod.setText(invoice.getPaymentMethod());
        txtQuantity.setText(String.valueOf(invoice.getTotalQuantity()));
        txtStatus.setText(invoice.getStatus());
        txtTime.setText(invoice.getInvoiceDate().toString());
        txtTotalAmount.setText(String.valueOf(invoice.getTotalAmount()));

        List<InvoiceDetail> list = dao.findByInvoiceID(invoice.getInvoiceID());

        for (InvoiceDetail invoiceDetail : list) {
            Product prod = prodDao.findById(invoiceDetail.getProductID());
            Object[] values = {
                prod.getProductID(),
                prod.getProductName(),
                invoiceDetail.getQuantity(),
                invoiceDetail.getUnitPrice(),};
            tbl.addRow(values);
        }
    }

