    public SelectCustomer(java.awt.Frame parent, boolean modal, SelectCustomerListener listener) {
        super(parent, modal);
        initComponents();
        fillToTable();
        btnSelect.addActionListener(e -> {
            Customer cus = list.get(tblCustomers.getSelectedRow());
            listener.onSelected(cus);
            this.dispose();
        });

        btnCreate.addActionListener(e -> {
            if (XDialog.confirm("Bạn có xác nhận muốn thêm khách hàng mới.")) {
                try {
                    Customer cus = getFormData();
                    if (cus != null) {
                        dao.create(cus);
                        listener.onSelected(dao.findByDate());
                        this.dispose();
                    }
                } catch (Exception ex) {
                    XDialog.alert("Có lỗi xảy ra.");
                }
            }

        });

    }

    public void fillToTable() {
        DefaultTableModel tbl = (DefaultTableModel) tblCustomers.getModel();
        tbl.setNumRows(0);
        for (Customer cus : list) {
            Object[] values = {
                cus.getCustomerID(),
                cus.getFullName(),
                cus.getPhone(),
                cus.getEmail(),};
            tbl.addRow(values);
        }
    }

