    public void filltoTable() {
        List<User> list = dao.findAll();
        DefaultTableModel tbl = (DefaultTableModel) tblUsers.getModel();
        tbl.setRowCount(0);
        for (User user : list) {
            String empName = "";
            for (Employee emp : listEmployee) {
                if (user.getEmployeeCode().equalsIgnoreCase(emp.getEmployeeID())) {
                    empName = emp.getFullName();
                }
            }
            Object[] values = {
                user.getUsername(),
                user.getPassword(),
                user.getEmployeeCode(),
                empName,
                user.getRole(),};
            tbl.addRow(values);
        }
    }

    @Override
    public void delete(String id) {
        dao.deleteById(id);
        filltoTable();
    }

    @Override
    public void update(String id) {
        User user = dao.findById(id);
        UpdateUserJDialog dialog = new UpdateUserJDialog(null, true, user);
        dialog.setLocationRelativeTo(null);
        dialog.setVisible(true);
        filltoTable();
    }
