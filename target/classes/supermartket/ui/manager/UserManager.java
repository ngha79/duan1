    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        List<User> list = dao.findByName(txtSearch.getText().trim());
        DefaultTableModel tbl = (DefaultTableModel) tblUsers.getModel();
        tbl.setRowCount(0);
        for (User user : list) {
            String empName = "";
            for (Employee emp : listEmployee) {
                if (user.getEmployeeCode().equalsIgnoreCase(emp.getEmployeeID())) {
                    empName = emp.getFullName();
                }
            }
            Object[] values = {
                user.getUsername(),
                user.getPassword(),
                user.getEmployeeCode(),
                empName,
                user.getRole(),};
            tbl.addRow(values);
        }
    }//GEN-LAST:event_btnSearchActionPerformed


